/*
* Map the time string to input. Needs to be HH:MM
*/
export function mapTimeStringToInput (timeString) {
  let time = timeString
  if (timeString && timeString.length > 5) {
    time = timeString.substring(0, 5)
  }
  return time
}

export function mapTimeToDisplay (timeString) {
  const time = timeString.split(':')
  const h = parseInt(time[0])
  const hours = (addZero(h) % 12) || 12
  const min = addZero(parseInt(time[1]))
  const meridiem = getMeridiem(h)

  return `${hours}:${min}${meridiem.toLowerCase()}`
}

/*
* Convert utc date time to local date
*
* @param [Date] dateTime. Date time. YYYY-MM-DDTHH:MM:ss
* @param [String] tzAbbr. Abbreviation of timezone, i.e. PST
* @param [String] YYYY-MM-DD
*/
export function convertUTCDatetimeToDateString(dateTime, tzAbbr) {
  const tz = mapTimezone(tzAbbr)
  const d = moment.tz(dateTime, tz).format('YYYY-MM-DD')
  return d
}

/*
* Convert utc date time to local datetime
*
* @param [Date] dateTime. Date time. YYYY-MM-DDTHH:MM:ss
* @param [String] tzAbbr. Abbreviation of timezone, i.e. PST
* @param [String] YYYY-MM-DD
*/
export function convertUTCDatetimeToDateTimeString(dateTime, tzAbbr) {
  const tz = mapTimezone(tzAbbr)
  const d = moment.tz(dateTime, tz).parseZone().format("ddd, MMM Do YYYY [at] h:mm a")
  return d
}

/*
* Convert local date to utc time.
*
* @param [String] dateString. The datestring to convert. YYYY-MM-DD
* @param [String] tzAbbr. Abbreviation of timezone, i.e. PST
* @return [Date]. Date time. YYYY-MM-DDTHH:MM:ss
*/
export function convertLocalDateToUTC (dateString, tzAbbr) {
  const tz = mapTimezone(tzAbbr)
  const d = moment.tz(dateString, tz).format()
  return d
}

/*
* Map timezone to full name
*
* @param [String] abbr. Timezone abbr, i.e. PDT
* @return [String]. Full timezone name, i.e. America/Los_Angeles
*/
export function mapTimezone (abbr) {
  const timezones = {
    'AKST': 'America/Januea',
    'AKDT': 'America/Juneau',
    'CDT': 'America/Chicago',
    'CST': 'America/Chicago',
    'EDT': 'America/New_York',
    'EST': 'America/New_York',
    'HDT': 'America/Adak',
    'HST': 'America/Adak',
    'MDT': 'America/Denver',
    'MST': 'America/Denver',
    'PDT': 'America/Los_Angeles',
    'PST': 'America/Los_Angeles'
  }
  return timezones[abbr.toUpperCase()]
}

